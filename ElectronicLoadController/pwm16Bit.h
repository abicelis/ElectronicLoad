// An implementation of a 16-bit analogWrite(). It uses TIMER1 and only works on pins 9 and 10, 
// as these are the only pins connected to the 16-bit timer.
// For more info: https://arduino.stackexchange.com/a/12719

/* Configure digital pins 9 and 10 as 16-bit PWM outputs. */
void setupILoadPWM16bit() {
    DDRB |= _BV(PB1) | _BV(PB2);        /* set pins as outputs */
    TCCR1A = _BV(COM1A1) | _BV(COM1B1)  /* non-inverting PWM */
        | _BV(WGM11);                   /* mode 14: fast PWM, TOP=ICR1 */
    TCCR1B = _BV(WGM13) | _BV(WGM12)
        | _BV(CS10);                    /* no prescaling */
    ICR1 = 0xffff;                      /* TOP counter value */
}

/* 16-bit version of analogWrite(). Works only on pins 9 and 10. */
void analogWrite16(uint8_t pin, uint16_t val)
{
    switch (pin) {
        case  9: OCR1A = val; break;
        case 10: OCR1B = val; break;
    }
}
